function setPath(obj, dotted, value) {
  const keys = dotted.split('.');
  let cur = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    if (!cur[keys[i]]) throw new Error('Missing path: ' + keys.slice(0, i + 1).join('.'));
    cur = cur[keys[i]];
  }
  cur[keys[keys.length - 1]] = value;
}

for (const loc of LOCALES) {
  const path = `./messages/${loc}.json`;
  const bundle = JSON.parse(readFileSync(path, 'utf8'));
  let count = 0;
  for (const [topNs, entries] of Object.entries(GAPS[loc])) {
    for (const [dotted, value] of Object.entries(entries)) {
      setPath(bundle[topNs], dotted, value);
      count++;
    }
  }
  writeFileSync(path, JSON.stringify(bundle, null, 2) + '\n');
  console.log(`wrote ${loc}.json: ${count} gap entries`);
}
