// Phase 2a — formatRelative replaced by Intl-backed wrapper. Bucket
// thresholds preserved (days → weeks → months) so callers don't change.
// The Intl output handles plural forms ("1 day ago" vs "5 days ago",
// or correct Russian noun cases) per locale automatically.
function relativeWhen(daysAgo: number, locale: Locale): string {
  if (daysAgo === 0) return formatRelativeTime(0, 'day', locale);
  if (daysAgo === 1) return formatRelativeTime(-1, 'day', locale);
  if (daysAgo < 7) return formatRelativeTime(-daysAgo, 'day', locale);
  if (daysAgo < 14) return formatRelativeTime(-1, 'week', locale);
  if (daysAgo < 30) return formatRelativeTime(-Math.floor(daysAgo / 7), 'week', locale);
  if (daysAgo < 60) return formatRelativeTime(-1, 'month', locale);
  return formatRelativeTime(-Math.floor(daysAgo / 30), 'month', locale);
}

